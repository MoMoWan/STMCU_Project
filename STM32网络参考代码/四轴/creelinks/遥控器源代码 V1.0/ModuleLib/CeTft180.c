/**
  * @brief  CeTft180绘制矩形
  * @param  ceTft180:CeTft180属性对象
  * @param  startX:矩形左上角x坐标
  * @param  startY:矩形左上角y坐标
  * @param  endX:矩形右下角x坐标
  * @param  endY:矩形右下角y坐标
  * @param  color:要显示的数据(颜色)
  * @ return None
  */
void ceTft180_drawRectangle(CeTft180* ceTft180, uint16 startX, uint16 startY, uint16 endX, uint16 endY, int16 color)
{
    int i, j;
    if (startX > endX || startY > endY)
    {
        return;
    }
    if (endX >= CE_TFT180_WIDTH)
    {
        endX = CE_TFT180_WIDTH-1;
    }
    if (endY > CE_TFT180_HIGHT)
    {
        endY = CE_TFT180_HIGHT - 1;
    }

#ifdef CE_TFT180_CAN_SET_REGIION
    ceTft180_setRegion(ceTft180, startX, startY, endX, endY);
    endX++;
    endY++;
    ceGpioOp.setBit(&(ceTft180->ceGpio1));
    ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
    for (i = startY; i<endY; i++)
    {
        for (j = startX; j<endX; j++)
        {
            ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, color >> 8);
            ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, color);
        }
    }
    ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
#else
    endX++;
    endY++;
    for (i = startY; i<endY; i++)
    {
        for (j = startX; j<endX; j++)
        {
            ceTft180_drawPoint(ceTft180, i, j, color);
        }
    }
#endif
}

/**
  * @brief  CeTft180绘制图片
  * @param  ceTft180:CeTft180属性对象
  * @param  x:图片的起始点x坐标
  * @param  y:图片的起始点y坐标
  * @param  colorBuf:图片数据
  * @param  bufSizeWidth:图片宽
  * @param  bufSizeHight:图片高
  * @ return None
  */
void ceTft180_drawData(CeTft180* ceTft180, uint16 x, uint16 y, const uint8* colorBuf, uint16 bufSizeWidth, uint16 bufSizeHight)
{
#ifdef CE_TFT180_CAN_SET_REGIION
    uint32 i;
    uint32 lgh = bufSizeHight * bufSizeWidth * 2;
    ceTft180_setRegion(ceTft180, x, y, x+ bufSizeWidth-1, y+ bufSizeHight-1);
    ceGpioOp.setBit(&(ceTft180->ceGpio1));
    ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
    for (i = 0; i < lgh; i+=2)
    {
        //ceTft180_writeUint16(ceTft180, (colorBuf[i])| (uint16)(colorBuf[i+1]<<8));
        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, colorBuf[i + 1]);
        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, colorBuf[i]);
    }
    ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
#else
    int i, j;
    uint16 temp;
    ceTft180_setRegion(ceTft180, x, y, x+ bufSizeWidth-1, y+ bufSizeHight-1);
    for (j = 0; j < bufSizeHight; j++)
    {
        for (i = 0; i < bufSizeWidth; i++)
        {
            temp = (uint16)colorBuf[(j*bufSizeWidth+i)*2+1] << 8 | colorBuf[(j*bufSizeWidth + i) * 2];
            ceTft180_drawPoint(ceTft180, x + i, y + j, temp);
        }
    }
#endif
}

