/**
  * @brief  向Tft控制IC写指令
  * @param  ceTft180:CeTft180属性对象
  * @return None
  */
void ceTft180_writeCom(CeTft180* ceTft180, uint8 com)
{
    ceGpioOp.resetBit(&(ceTft180->ceGpio1));
    ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, com);
    ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
}

/**
  * @brief  向Tft控制IC写8位数据
  * @param  ceTft180:CeTft180属性对象
  * @return None
  */
void ceTft180_writeData(CeTft180* ceTft180, uint8 data)
{
    ceGpioOp.setBit(&(ceTft180->ceGpio1));
    ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, data);
    ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));

}

/**
  * @brief  向Tft控制IC写16位数据(此模块内部写数据都是16位，为提高效率，多次用到此方法的地方直接内部代码替换了。)
  * @param  ceTft180:CeTft180属性对象
  * @return None
  */
void ceTft180_writeUint16(CeTft180* ceTft180, uint16 data)
{
    ceGpioOp.setBit(&(ceTft180->ceGpio1));
    ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, data >> 8);
    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, data);
    ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
}

/**
  * @brief  CeTft180模块复位
  * @param  ceTft180:CeTft180属性对象
  * @return None
  */
void ceTft180_tftReset(CeTft180* ceTft180)
{
    ceGpioOp.resetBit(&(ceTft180->ceGpio0));
    ceSystemOp.delayMs(10);
    ceGpioOp.setBit(&(ceTft180->ceGpio0));
    ceSystemOp.delayMs(10);
}

/**
  * @brief  CeTft180模块初始化
  * @param  ceTft180:CeTft180属性对象
  * @param  ceXX:CeTft180模块使用的资源号
  * @return 系统状态码
  */
void ceTft180_tftInitial(CeTft180* ceTft180)
{
    uint16 i,j;
    for (j = 0; j < CE_TFT180_WIDTH / 6; j++)
    {
        for (i = 0; i < CE_TFT180_HIGHT / 8; i++)
        {
            ceTft180->asBuf[j][i] = ' ';
        }
    }
    ceTft180->asXIndex = 0;
    ceTft180->asYIndex = 0;

    ceTft180_tftReset(ceTft180);//Reset before LCD Init.

    ceTft180_writeCom(ceTft180, 0x11);//Exit Sleep
    ceSystemOp.delayMs(120);
    ceTft180_writeCom(ceTft180, 0x26);//Set Default Gamma
    ceTft180_writeData(ceTft180, 0x04);
    ceTft180_writeCom(ceTft180, 0xF2);//E0h & E1h Enable/Disable
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeCom(ceTft180, 0xB1);
    ceTft180_writeData(ceTft180, 0x0C);
    ceTft180_writeData(ceTft180, 0x14);
    ceTft180_writeCom(ceTft180, 0xC0);//Set VRH1[4:0] & VC[2:0] for VCI1 & GVDD
    ceTft180_writeData(ceTft180, 0x0C);
    ceTft180_writeData(ceTft180, 0x05);
    ceTft180_writeCom(ceTft180, 0xC1);//Set BT[2:0] for AVDD & VCL & VGH & VGL
    ceTft180_writeData(ceTft180, 0x02);
    ceTft180_writeCom(ceTft180, 0xC5);//Set VMH[6:0] & VML[6:0] for VOMH & VCOML
    ceTft180_writeData(ceTft180, 0x32);
    ceTft180_writeData(ceTft180, 0x3B);
    ceTft180_writeCom(ceTft180, 0xC7);
    ceTft180_writeData(ceTft180, 0x40);
    ceTft180_writeCom(ceTft180, 0x2A);//Set Column Address
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x7F);
    ceTft180_writeCom(ceTft180, 0x2B);//Set Page Address
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x00);
    ceTft180_writeData(ceTft180, 0x9F);
    ceTft180_writeCom(ceTft180, 0x36);//Set Scanning Direction
    ceTft180_writeData(ceTft180, 0xC0);
    ceTft180_writeCom(ceTft180, 0x3A);
    ceTft180_writeData(ceTft180, 0x55);

    ceTft180_fill(ceTft180, CE_TFT180_COLOR_BLACK);//在显示前先将用于显示的寄存器清空，以免不断电复位情况下，显示出之前的数据
    ceSystemOp.delayUs(10);

    ceTft180_writeCom(ceTft180, 0x29);// Display On
}

/**
  * @brief  设置绘制区域
  * @param  ceTft180:CeTft180属性对象
  * @param  startX:矩形左上角x坐标
  * @param  startY:矩形左上角y坐标
  * @param  endX:矩形右下角x坐标
  * @param  endY:矩形右下角y坐标
  * @ return None
  */
void ceTft180_setRegion(CeTft180* ceTft180, uint16 startX, uint16 startY, uint16 endX, uint16 endY)
{
    ceTft180_writeCom(ceTft180, 0x2A);
    ceTft180_writeData(ceTft180, ((startX) >> 8) & 0xFF);
    ceTft180_writeData(ceTft180, (startX) & 0xFF);
    ceTft180_writeData(ceTft180, ((endX) >> 8) & 0xFF);
    ceTft180_writeData(ceTft180, (endX) & 0xFF);

    ceTft180_writeCom(ceTft180, 0x2B);
    ceTft180_writeData(ceTft180, ((startY) >> 8) & 0xFF);
    ceTft180_writeData(ceTft180, (startY) & 0xFF);
    ceTft180_writeData(ceTft180, ((endY) >> 8) & 0xFF);
    ceTft180_writeData(ceTft180, (endY) & 0xFF);

    ceTft180_writeCom(ceTft180, 0x2C);
}

/**
  * @ brief  CeTft180显示字符
  * @ param  ceTft180:CeTft180属性对象
  * @ param  x:设置开始显示字符的x轴坐标
  * @ param  y:设置开始显示字符的y轴坐标
  * @ param  foreColor:显示字体的前景色
  * @ param  backColor:显示字体的背景色
  * @ param  msg:要显示的字符串指针
  * @ param showSize:显示的字体大小，可选CE_TFT320_EN_SIZE_F6X8或CE_TFT320_EN_SIZE_F8X16
  * @ return None
  */
void ceTft180_showChar(CeTft180* ceTft180, uint16 x, uint16 y, uint16 foreColor, uint16 backColor, const char ch, CE_TFT180_EN_SIZE showSize)
{
#ifdef CE_TFT180_CAN_SET_REGIION
    if (showSize == CE_TFT180_EN_SIZE_F6X8)
    {
        uint8  i, j, c, index;
        c = ch - 32;
        ceTft180_setRegion(ceTft180, x,  y, x +6-1,y + 8-1);
        index = 0x01;
        ceGpioOp.setBit(&(ceTft180->ceGpio1));
        ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
        for (i = 0; i < 8; i++)
        {
            for (j = 0; j < 6; j++)
            {
                if((CE_TFT180_F6x8[c][j] & index) == index)
                {
                    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, foreColor >> 8);
                    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, foreColor);
                }
                else
                {
                    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, backColor >> 8);
                    ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, backColor);
                }
            }
            index <<= 1;
        }
        ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
    }
    else
    {
        uint8 i, j, c, m, index;
        c = ch - 32;
        ceTft180_setRegion(ceTft180, x,  y, x + 8 - 1,y + 16 - 1);
        ceGpioOp.setBit(&(ceTft180->ceGpio1));
        ceSpiMasterOp.resetNSSBit(&(ceTft180->ceSpi));
        for (i = 0; i < 2; i++)
        {
            index = 0x01;
            for (j = 0; j < 8; j++)
            {
                for (m = 0; m < 8; m++)
                {
                    if ((CE_TFT180_F8X16[c * 16 +i*8+m] & index) == index)
                    {
                        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, foreColor >> 8);
                        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, foreColor);
                    }
                    else
                    {
                        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, backColor >> 8);
                        ceSpiMasterOp.writeReadByte(&ceTft180->ceSpi, backColor);
                    }
                }
                index <<= 1;
            }
        }
        ceSpiMasterOp.setNSSBit(&(ceTft180->ceSpi));
    }
#else
    uint8  j, m, n, c, temp, index;
    c = ch - 32;
    if (showSize == CE_TFT180_EN_SIZE_F6X8)
    {
        ceTft180_setRegion(ceTft180, x,  y, x +6-1,y + 8-1);
        for (j = 0; j < 6; j++)
        {
            temp = CE_TFT180_F6x8[c][j];
            index = 0x01;
            for (m = 0; m < 8; m++)
            {
                if ((temp & index) == index)
                {
                    ceTft180_drawPoint(ceTft180, x + j, y + m, foreColor);
                }
                else
                {
                    ceTft180_drawPoint(ceTft180, x + j, y + m, backColor);
                }
                index <<= 1;
            }
        }
        x += 6;
    }
    else
    {
        ceTft180_setRegion(ceTft180, x, y, x + 8 - 1, y + 16 - 1);
        for (j = 0; j < 2; j++)
        {
            for (m = 0; m < 8; m++)
            {
                temp = CE_TFT180_F8X16[c * 16 + j * 8 + m];
                index = 0x01;
                for (n = 0; n < 8; n++)
                {
                    if ((temp & index) == index)
                    {
                        ceTft180_drawPoint(ceTft180, x + m, y + n + j * 8, foreColor);
                    }
                    else
                    {
                        ceTft180_drawPoint(ceTft180, x + m, y + n + j * 8, backColor);
                    }
                    index <<= 1;
                }
            }
        }
        x += 8;
    }
#endif
}


